/**
 * This function will calculate the area of a square 
 * @param width
 * @param height
 * @return 
 */
public int validateAreaOfSquare( int width, int height ) {

    if( width < 0 ) 
    { return -1; }

    if( height < 0 ) 
    { return -1; }



    
    int area = width * height;



return area;
}


        
        
        
        
        
        
